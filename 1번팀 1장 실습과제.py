t.fillcolor("yellow")
t.begin_fill()
t.left(90)
t.forward(200)
t.left(90)
t.forward(100)
t.left(90)
t.forward(200)
t.left(90)
t.forward(100)
t.end_fill()
"""지붕 만들기 끝
왼쪽 바퀴 만들기 시작"""
t.up()
