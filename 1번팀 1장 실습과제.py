#실습문제 1(자동차)
import turtle as t
t.shape("turtle")
#몸체만들기 시작
t.up()
t.goto(-200,-100)
t.color("black")
t.fillcolor("red")
t.down()
t.begin_fill()
t.forward(400)
t.left(90)
t.forward(200)
t.left(90)
t.forward(400)
t.left(90)
t.forward(200)
t.end_fill()
"""몸체만들기끝
지붕만들기시작"""
t.up()
